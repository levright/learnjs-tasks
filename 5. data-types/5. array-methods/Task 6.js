/*
Создайте функцию конструктор Calculator, которая создаёт "расширяемые" 
объекты калькулятора.

Задание состоит из двух частей.

1.
Во-первых, реализуйте метод calculate(str), который принимает строку 
типа "1 + 2" в формате «ЧИСЛО оператор ЧИСЛО» (разделено пробелами) 
и возвращает результат. Метод должен понимать плюс + и минус -.

2.
Затем добавьте метод addMethod(name, func), который добавляет 
в калькулятор новые операции. Он принимает оператор name и функцию 
с двумя аргументами func(a,b), которая описывает его.

	
	Для этой задачи не нужны скобки или сложные выражения.
	Числа и оператор разделены ровно одним пробелом.
	Не лишним будет добавить обработку ошибок.

*/

function Calculator() {
	
	this.methods = {
		"-": (a, b) => a - b,
		"+": (a, b) => a + b,
	};

	this.calculate = function(str) {
		let split = str.split(" ");
		let a = +split[0];
		let op = split[1];
		let b = +split[2];
	
		return (!this.methods[op] || isNaN(a) || isNaN(b)) ? NaN : this.methods[op](a, b);
	};

	this.addMethod = function(name, fun) {
		this.methods[name] = fun;
	};
}